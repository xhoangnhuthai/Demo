package Controller;

import java.io.IOException;
import java.io.PrintWriter;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;

/**
 *
 * @author Admin
 */
public class LoginServlet extends HttpServlet {
    private static final long serialVersionUID = 1L;
       
    public LoginServlet() {
        super();
    }

	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		String username = request.getParameter("username");
		String password = request.getParameter("password");
		
		// Kiểm tra username và password
		if (username.equals(getServletContext().getInitParameter("username"))
				&& password.equals(getServletContext().getInitParameter("password"))) {
			// Tạo session và forward tới WelcomeServlet
			HttpSession session = request.getSession();
			session.setAttribute("username", username);
			request.getRequestDispatcher("WelcomeServlet").forward(request, response);
		} else {
			// Redirect lại trang đăng nhập nếu sai username hoặc password
			response.sendRedirect("index.jsp");
		}
	}

	protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		doGet(request, response);
	}

}

